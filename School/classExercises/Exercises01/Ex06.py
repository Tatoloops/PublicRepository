"""
Desarrolle un programa que solicite el ingreso de una letra y determine si es una
vocal o no.
"""
A=input("Declare a letter of the abecedary: ")
if A=='A' or A=='E' or A=='I' or A=='O' or A=='U' or A=='a' or A=='e' or A=='i' or A=='o' or A=='u':
	print(f"{A} is a vowel")
else:
	print(f"{A} is not a vowel")